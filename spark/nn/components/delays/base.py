from __future__ import annotations
from typing import TYPE_CHECKING
if TYPE_CHECKING:
    from spark.core.config import SparkConfig

import abc
import typing as tp
from spark.core.payloads import SpikeArray 
from spark.nn.components.base import Component

#################################################################################################################################################
#-----------------------------------------------------------------------------------------------------------------------------------------------#
#################################################################################################################################################

# Generic Delays output contract.
class DelaysOutput(tp.TypedDict):
    out_spikes: SpikeArray

#-----------------------------------------------------------------------------------------------------------------------------------------------#

class Delays(Component):
    """
        Abstract synaptic delay model.
    """

    def __init__(self, config: SparkConfig = None, **kwargs):
        # Initialize super.
        super().__init__(config = config, **kwargs)
    
    @abc.abstractmethod
    def _push(self, spikes: SpikeArray) -> None:
        """
            Push operation.
        """
        pass

    @abc.abstractmethod
    def _gather(self,) -> SpikeArray:
        """
            Gather operation.
        """
        pass

    @abc.abstractmethod
    def reset(self) -> None:
        """
            Resets component state.
        """
        pass

    @abc.abstractmethod
    def __call__(self, in_spikes: SpikeArray) -> DelaysOutput:
        pass

