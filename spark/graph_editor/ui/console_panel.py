class ConsolePanel(QtWidgets.QDockWidget):
    """
        Container for the Console for debug purposes.
    """

    def __init__(self, **kwargs):
        super().__init__(**kwargs)
        self.setAllowedAreas(
            QtCore.Qt.DockWidgetArea.BottomDockWidgetArea
        )
        # Header
        self.setTitleBarWidget(DockTitleBar('Console'))

