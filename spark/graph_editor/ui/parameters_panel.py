class ParametersPanel(QtWidgets.QDockWidget):
    """
        Container for the Parameters (Input/Output) variables description.
    """

    def __init__(self, **kwargs):
        super().__init__(**kwargs)
        self.setAllowedAreas(
            QtCore.Qt.DockWidgetArea.LeftDockWidgetArea | QtCore.Qt.DockWidgetArea.RightDockWidgetArea
        )
        # Header
        self.setTitleBarWidget(DockTitleBar('Parameters'))

